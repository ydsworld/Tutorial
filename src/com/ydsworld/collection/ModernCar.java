package com.ydsworld.collection;

import java.util.List;

public class ModernCar {
	private String year;
	private List<Car> workingCar;
	
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public List<Car> getWorkingCar() {
		return workingCar;
	}
	public void setWorkingCar(List<Car> workingCar) {
		this.workingCar = workingCar;
	}
}
